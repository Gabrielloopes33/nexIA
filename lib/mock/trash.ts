export function calcularDiasRestantes(expiracaoEm: string): number {
  const expiracao = new Date(expiracaoEm)
  const hoje = new Date()
  const diff = expiracao.getTime() - hoje.getTime()
  return Math.max(0, Math.ceil(diff / (1000 * 60 * 60 * 24)))
}

